package sacher.datamodel;

public class Attivita {

	private String attivita;
	private String ambito;
	private String subattivita;
	private String l1;
	private String l0;

	public String getAttivita() {
		return attivita;
	}

	public void setAttivita(String attivita) {
		this.attivita = attivita;
	}

	public String getAmbito() {
		return ambito;
	}

	public void setAmbito(String ambito) {
		this.ambito = ambito;
	}

	public String getSubattivita() {
		return subattivita;
	}

	public void setSubattivita(String subattivita) {
		this.subattivita = subattivita;
	}

	public String getL1() {
		return l1;
	}

	public void setL1(String l1) {
		this.l1 = l1;
	}

	public String getL0() {
		return l0;
	}

	public void setL0(String l0) {
		this.l0 = l0;
	}

}
