package sacher.datamodel;

public class Date {

	private String date;
	private String timestamp;
	private String weekend;
	private String day_of_week;
	private String month;
	private String month_day;
	private String year;
	private String week_starting_monday;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getWeekend() {
		return weekend;
	}

	public void setWeekend(String weekend) {
		this.weekend = weekend;
	}

	public String getDay_of_week() {
		return day_of_week;
	}

	public void setDay_of_week(String day_of_week) {
		this.day_of_week = day_of_week;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getMonth_day() {
		return month_day;
	}

	public void setMonth_day(String month_day) {
		this.month_day = month_day;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getWeek_starting_monday() {
		return week_starting_monday;
	}

	public void setWeek_starting_monday(String week_starting_monday) {
		this.week_starting_monday = week_starting_monday;
	}

}
